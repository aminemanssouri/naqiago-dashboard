// Helper function to get customer name by ID
export const getCustomerName = (customerId) => {
  const customer = dummyCustomers.find(c => c.id === customerId);
  return customer ? customer.full_name : "Unknown Customer";
};

// Helper function to get service title by ID
export const getServiceTitle = (serviceId) => {
  const service = dummyServices.find(s => s.id === serviceId);
  return service ? service.title : "Unknown Service";
};

// Helper function to get booking by ID
export const getBookingById = (bookingId) => {
  return dummyBookings.find(b => b.id === bookingId);
};

// Status color helpers
export const getStatusColor = (status) => {
  const colors = {
    pending: "bg-yellow-100 text-yellow-800",
    confirmed: "bg-blue-100 text-blue-800", 
    in_progress: "bg-orange-100 text-orange-800",
    completed: "bg-green-100 text-green-800",
    cancelled: "bg-red-100 text-red-800",
  };
  return colors[status] || "bg-gray-100 text-gray-800";
};

export const getPaymentStatusColor = (status) => {
  const colors = {
    pending: "bg-yellow-100 text-yellow-800",
    processing: "bg-blue-100 text-blue-800",
    completed: "bg-green-100 text-green-800",
    failed: "bg-red-100 text-red-800",
    refunded: "bg-purple-100 text-purple-800",
  };
  return colors[status] || "bg-gray-100 text-gray-800";
};